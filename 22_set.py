# সেট এর আইটেম গুলাকে index number দিয়ে এক্সেস করা যাবেনা 
# সেট এর মাঝে ডুব্লিকেট ভ্যালু রাখা যাবেনা 
# সেট ক্রিয়েট করার ২টি অপশন আছে 1.{সেকেন্ড ব্রাকেট দিয়ে} 2.{সেট ফাংশন ব্যবহার করতে পারবো}

num = {1,2,3,4,5,5}
# print(num)

num2 = set([4,5,6,6])
# print(num2)

# union দুটা সেটের সবগুলা ভ্যালু দেখাবে এবং যেগুলা কমন থাকবে সেগুলা একবার দেখাবে
print(num | num2)  

# intersection দুটা সেটের মাঝে কমন ভ্যালু গুলাই প্রিন্ট করবে 
print(num & num2) 

